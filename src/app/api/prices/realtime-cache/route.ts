import { NextResponse } from 'next/server';

const COINGECKO_API_KEY = 'CG-9W1U48SPhUME6EeyinMWDtJs';
const COINGECKO_BASE_URL = 'https://api.coingecko.com/api/v3';

// Cache compartilhado para pre√ßos em tempo real
let REALTIME_PRICES_CACHE: { [symbol: string]: any } = {};
let CACHE_TIMESTAMP = 0;
const CACHE_DURATION = 10 * 1000; // 10 segundos de cache

// Fun√ß√£o para buscar pre√ßos em tempo real com cache compartilhado
async function getRealTimePricesWithCache(symbols: string[]) {
  const now = Date.now();
  
  // Verificar se o cache ainda √© v√°lido
  if (Object.keys(REALTIME_PRICES_CACHE).length > 0 && (now - CACHE_TIMESTAMP) < CACHE_DURATION) {
    console.log('üì¶ Usando cache compartilhado de pre√ßos:', Object.keys(REALTIME_PRICES_CACHE).length, 'tokens');
    
    // Verificar quais s√≠mbolos est√£o no cache e quais precisam ser buscados
    const cachedSymbols = symbols.filter(symbol => REALTIME_PRICES_CACHE[symbol]);
    const missingSymbols = symbols.filter(symbol => !REALTIME_PRICES_CACHE[symbol]);
    
    console.log('üì¶ S√≠mbolos no cache:', cachedSymbols);
    console.log('‚ùì S√≠mbolos faltando:', missingSymbols);
    
    // Se todos os s√≠mbolos est√£o no cache, retornar do cache
    if (missingSymbols.length === 0) {
      const cachedResults = symbols.map(symbol => {
        const cached = REALTIME_PRICES_CACHE[symbol];
        return {
          symbol,
          success: true,
          realTimePrice: cached.price,
          priceChange24h: cached.priceChange24h,
          lastUpdated: cached.lastUpdated
        };
      });
      
      return cachedResults;
    }
    
    // Se alguns s√≠mbolos est√£o faltando, buscar apenas eles e manter o cache
    console.log('üîÑ Buscando s√≠mbolos faltando:', missingSymbols);
    const missingResults = await fetchMissingTokens(missingSymbols);
    
    // Combinar resultados do cache com os novos
    const allResults = symbols.map(symbol => {
      if (REALTIME_PRICES_CACHE[symbol]) {
        // Usar cache existente
        const cached = REALTIME_PRICES_CACHE[symbol];
        return {
          symbol,
          success: true,
          realTimePrice: cached.price,
          priceChange24h: cached.priceChange24h,
          lastUpdated: cached.lastUpdated
        };
      } else {
        // Usar resultado da busca
        const result = missingResults.find(r => r.symbol === symbol);
        return result || {
          symbol,
          success: false,
          error: 'Token n√£o encontrado'
        };
      }
    });
    
    return allResults;
  }

  // Cache expirado ou vazio, buscar novos dados
  console.log('üîÑ Cache expirado, buscando novos pre√ßos da CoinGecko...');
  
  try {
    // Buscar todos os tokens da CoinGecko (usando a mesma l√≥gica do buscador)
    const allTokens = await getAllTokensFromCoinGecko();
    
    // Processar os s√≠mbolos solicitados
    const results = symbols.map(symbol => {
      const token = allTokens.find(t => t.symbol === symbol.toUpperCase());
      
      if (token) {
        const result = {
          symbol,
          success: true,
          realTimePrice: token.currentPrice,
          priceChange24h: token.priceChange24h,
          lastUpdated: new Date().toISOString()
        };
        
        // Adicionar ao cache
        REALTIME_PRICES_CACHE[symbol] = {
          price: token.currentPrice,
          priceChange24h: token.priceChange24h,
          lastUpdated: new Date().toISOString()
        };
        
        return result;
      } else {
        return {
          symbol,
          success: false,
          error: 'Token n√£o encontrado'
        };
      }
    });
    
    // Atualizar timestamp do cache
    CACHE_TIMESTAMP = now;
    
    console.log('‚úÖ Cache compartilhado atualizado:', Object.keys(REALTIME_PRICES_CACHE).length, 'tokens');
    
    return results;
  } catch (error) {
    console.error('‚ùå Erro ao buscar pre√ßos em tempo real:', error);
    
    // Se o cache tem dados antigos, usar eles mesmo assim
    if (Object.keys(REALTIME_PRICES_CACHE).length > 0) {
      console.log('üîÑ Usando cache antigo devido ao erro');
      return symbols.map(symbol => {
        const cached = REALTIME_PRICES_CACHE[symbol];
        if (cached) {
          return {
            symbol,
            success: true,
            realTimePrice: cached.price,
            priceChange24h: cached.priceChange24h,
            lastUpdated: cached.lastUpdated
          };
        } else {
          return {
            symbol,
            success: false,
            error: 'Token n√£o encontrado'
          };
        }
      });
    }
    
    return symbols.map(symbol => ({
      symbol,
      success: false,
      error: 'Erro na API'
    }));
  }
}

// Fun√ß√£o para buscar apenas tokens faltantes (sem buscar toda a lista)
async function fetchMissingTokens(missingSymbols: string[]) {
  try {
    console.log('üîç Buscando tokens faltantes:', missingSymbols);
    
    // Buscar apenas os tokens espec√≠ficos que est√£o faltando
    const results = [];
    
    for (const symbol of missingSymbols) {
      try {
        // Buscar token espec√≠fico por s√≠mbolo
        const response = await fetch(`${COINGECKO_BASE_URL}/coins/markets?vs_currency=usd&ids=${symbol.toLowerCase()}&order=market_cap_desc&per_page=1&sparkline=false&price_change_percentage=24h`, {
          headers: {
            'Accept': 'application/json',
            'User-Agent': 'SenhorBarriga-Portfolio/1.0',
            'X-CG-API-KEY': COINGECKO_API_KEY
          },
          cache: 'no-store'
        });

        if (response.ok) {
          const data = await response.json();
          if (data.length > 0) {
            const token = data[0];
            const result = {
              symbol,
              success: true,
              realTimePrice: token.current_price,
              priceChange24h: token.price_change_percentage_24h,
              lastUpdated: new Date().toISOString()
            };
            
            // Adicionar ao cache
            REALTIME_PRICES_CACHE[symbol] = {
              price: token.current_price,
              priceChange24h: token.price_change_percentage_24h,
              lastUpdated: new Date().toISOString()
            };
            
            results.push(result);
            console.log(`‚úÖ ${symbol}: $${token.current_price}`);
          } else {
            results.push({
              symbol,
              success: false,
              error: 'Token n√£o encontrado'
            });
            console.log(`‚ùå ${symbol}: n√£o encontrado`);
          }
        } else {
          results.push({
            symbol,
            success: false,
            error: `Erro API: ${response.status}`
          });
          console.log(`‚ùå ${symbol}: erro ${response.status}`);
        }
        
        // Delay pequeno entre requisi√ß√µes
        await new Promise(resolve => setTimeout(resolve, 50));
        
      } catch (error) {
        console.error(`‚ùå Erro ao buscar ${symbol}:`, error);
        results.push({
          symbol,
          success: false,
          error: 'Erro de rede'
        });
      }
    }
    
    return results;
  } catch (error) {
    console.error('‚ùå Erro ao buscar tokens faltantes:', error);
    return missingSymbols.map(symbol => ({
      symbol,
      success: false,
      error: 'Erro interno'
    }));
  }
}

// Fun√ß√£o para buscar todos os tokens da CoinGecko (copiada do buscador)
async function getAllTokensFromCoinGecko() {
  try {
    console.log('üîÑ Buscando todos os tokens da CoinGecko...');
    
    let allTokens: any[] = [];
    
    // Buscar at√© 60 p√°ginas (15.000 tokens)
    for (let page = 1; page <= 60; page++) {
      const response = await fetch(`${COINGECKO_BASE_URL}/coins/markets?vs_currency=usd&order=market_cap_desc&per_page=250&page=${page}&sparkline=false&price_change_percentage=24h`, {
        headers: {
          'Accept': 'application/json',
          'User-Agent': 'SenhorBarriga-Portfolio/1.0',
          'X-CG-API-KEY': COINGECKO_API_KEY
        },
        cache: 'no-store',
        next: { revalidate: 300 }
      });

      if (response.ok) {
        const data = await response.json();
        allTokens = allTokens.concat(data);
        console.log(`üìÑ P√°gina ${page}: ${data.length} tokens encontrados`);
        
        if (data.length === 0) break;
        
        // Delay para evitar rate limiting
        if (page < 60) {
          await new Promise(resolve => setTimeout(resolve, 100));
        }
      } else {
        console.log(`‚ö†Ô∏è Erro na p√°gina ${page}:`, response.status);
        if (response.status === 429) {
          console.log('üö´ Rate limit atingido');
        }
        break;
      }
    }
    
    return allTokens.map(coin => ({
      id: coin.id,
      name: coin.name,
      symbol: coin.symbol.toUpperCase(),
      currentPrice: coin.current_price,
      priceChange24h: coin.price_change_percentage_24h
    }));
  } catch (error) {
    console.error('‚ùå Erro ao buscar tokens:', error);
    return [];
  }
}

// Handler da API
export async function POST(request: Request) {
  try {
    const { symbols } = await request.json();
    
    if (!symbols || !Array.isArray(symbols)) {
      return NextResponse.json({ 
        success: false, 
        error: 'Lista de s√≠mbolos √© obrigat√≥ria' 
      }, { status: 400 });
    }
    
    console.log('üìä Buscando pre√ßos em tempo real para:', symbols);
    
    const results = await getRealTimePricesWithCache(symbols);
    
    return NextResponse.json({
      success: true,
      results,
      cacheInfo: {
        cachedTokens: Object.keys(REALTIME_PRICES_CACHE).length,
        cacheAge: Date.now() - CACHE_TIMESTAMP,
        cacheDuration: CACHE_DURATION
      }
    });
  } catch (error) {
    console.error('‚ùå Erro na API de cache compartilhado:', error);
    return NextResponse.json({ 
      success: false, 
      error: 'Erro interno do servidor' 
    }, { status: 500 });
  }
}

// Endpoint GET para verificar status do cache
export async function GET() {
  return NextResponse.json({
    success: true,
    cacheInfo: {
      cachedTokens: Object.keys(REALTIME_PRICES_CACHE).length,
      cacheAge: Date.now() - CACHE_TIMESTAMP,
      cacheDuration: CACHE_DURATION,
      isExpired: (Date.now() - CACHE_TIMESTAMP) >= CACHE_DURATION
    }
  });
}
